// Obtiene las coordenadas de un aeropuerto por su código IATA
export const getAirportCoordinates = (iataCode) => {
  if (!iataCode) return null;
  
  const code = iataCode.toUpperCase();
  return AIRPORT_COORDINATES[code] || null;
};

// Verifica si un aeropuerto existe en la base de datos
export const hasAirportCoordinates = (iataCode) => {
  if (!iataCode) return false;
  return AIRPORT_COORDINATES[iataCode.toUpperCase()] !== undefined;
};

// Obtiene una lista de todos los aeropuertos disponibles
export const getAllAirports = () => {
  return Object.entries(AIRPORT_COORDINATES).map(([code, data]) => ({
    code,
    ...data
  }));
};
